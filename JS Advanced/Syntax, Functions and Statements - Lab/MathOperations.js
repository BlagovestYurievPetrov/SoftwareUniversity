function calculate(n1, n2, op) {
    switch (op) {
      case '+':
        console.log(n1 + n2);
        break;
      case '-':
        console.log(n1 - n2);
        break;
        case "/":
      console.log(n1/n2);
      break;
      case "*":
      console.log(n1*n2);
      break;
      case "**":
      console.log(n1**n2);
      break;
      case "%":
      console.log(n1%n2);
      break;
    }
  }
  